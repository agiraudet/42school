char	*ft_strcat(char *dest, char *src)
{
	int i;
	int ii;

	i = 0;
	ii = 0;
	while (dest[i] != '\0')
		i++;
	while (src[ii] != '\0')
		dest[i++] = src[ii++];
	dest[i] = '\0';
	return (dest);
}
