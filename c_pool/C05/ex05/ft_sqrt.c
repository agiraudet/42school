int	ft_sqrt(int nb)
{
	int		i;
	double	x;

	i = 0;
	x = (nb + 1) / 2;
	if (nb <= 0)
		return (0);
	while (i < 50)
	{
		x = (x + nb / x) / 2;
		i++;
	}
	if ((int)x * (int)x == nb)
		return ((int)x);
	else
		return (0);
}
